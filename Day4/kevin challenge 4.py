full_name=input("Please enter your full name:")
print(f"Uppercase:{full_name.upper()}")   #to print the input string in capital letters
print(f"Lowercase:{full_name.lower()}")   #to print the input string in small letters
print(f"Capitalized:{full_name.capitalize()}") #to print the first letter of the input string in capital