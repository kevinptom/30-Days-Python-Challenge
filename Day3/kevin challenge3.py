num1=int(input("Enter the first number:"))
num2=int(input("Enter the second number:"))

sum=num1+num2 #adding num1 and num2
print("The sum of",num1,"and",num2,"is",sum)   #printing the sum

difference=num1-num2  #to find the difference when num2 is subtracted from num1
print("The difference when",num2,"is subtracted from",num1,"is",difference) #printing the difference

product=num1*num2 #to find the product
print("The product of",num1,"and",num2,"is",product)  #printing the product

quotient=num1/num2  #to find the quotient when num1 is divided by num2
print("The quotient of",num1,"divided by",num2,"is",quotient)  #printing the quotient

remainder=num1%num2  #to find the remainder when num1 is divided by num2
print("The remainder when",num1,"divided by",num2,"is",remainder) #printing the remainder
